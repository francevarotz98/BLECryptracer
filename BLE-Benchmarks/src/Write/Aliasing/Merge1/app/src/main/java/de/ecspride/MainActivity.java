	class A{
		public byte[] b = new byte[0];
	}

	public class B{
		public A attr;
	}

	private void aliasFlowTest() {
		String charValue = "This is a test....";
		String toWrite = charValue.toString();
		SecretKey secret = generateKey("*****");
		byte[] bytesToWrite = new byte[0];
		try {
			bytesToWrite = encryptMsg(toWrite, secret); // bytesToWrite is the source
		} catch (NoSuchAlgorithmException e) {
			e.printStackTrace();
		} catch (NoSuchPaddingException e) {
			e.printStackTrace();
		} catch (InvalidKeyException e) {
			e.printStackTrace();
		} catch (InvalidParameterSpecException e) {
			e.printStackTrace();
		} catch (IllegalBlockSizeException e) {
			e.printStackTrace();
		} catch (BadPaddingException e) {
			e.printStackTrace();
		} catch (UnsupportedEncodingException e) {
			e.printStackTrace();
		}	// source
        
        A b, q, y;
		B a, p, x;
		
		a = new B();
		p = new B();
		
		b = new A();
		q = new A();
		
		if (Math.random() < 0.5) {
			x = a;
			y = b;
		}
		else {
			x = p;
			y = q;
		}
		x.attr = y;
		q.b = bytesToWrite;

		mBluetoothGattCharacteristic.setValue(a.attr.b); // sink
	}

